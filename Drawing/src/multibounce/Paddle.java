package multibounce;

public class Paddle {

	int paddleXX;
	int paddleYY;
	int paddleWidth;
	int paddleHeight;
	
	Paddle(int paddleXX, int paddleYY, int paddleWidth, int paddleHeight) {
		this.paddleXX = paddleXX;
		this.paddleYY = paddleYY;
		this.paddleWidth = paddleWidth;
		this.paddleHeight = paddleHeight;
	}
	
}
